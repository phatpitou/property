            </p><p>It integrates business, shopping, catering, entertainment and leisure, creating the best, most comfortable and most convenient life in Cambodia. This new project features the latest in building design, having secured the services of top architectural, design, and construction firms. Prince International Plaza will primarily consist of five 12-story buildings with a total building area of 200,000 sqm. 
            </p><p>
            </p><p>Within these buildings there will be Grade 5A offices, star-rated convention centers, supermarkets, commercial spaces, and a luxurious residential complex. There would also be restaurants, sports facilities, entertainment areas, bars, children&amp;rsquo;s playground, and function rooms. The Grade A offices occupy 630,000 sqm of the total project area. Targeted to be completed and delivered by the end of 2020, Prince International Plaza will have 456 luxury condominiums, with one to three bedrooms, sizes ranging from 50 sqm to 160 sqm. 
            </p><p>
            </p><p>Each unit will have top-flight furnishings and modern, sleek furniture. Prince International Plaza houses a star-rated fitness club and an infinity swimming pool, and provides the best views of Phnom Penh and surrounding areas. It also provides customer service, 24-hour one-stop security service that ensures occupants are safe and secure for the duration of their stay. 
            </p><p>
            </p><p>Occupants and their guests will also have access to more than 1,000 parking spaces. The design and features of the condos ensure that its occupants will be pampered with the best money can buy. It will be an &amp;lsquo;oasis&amp;rsquo; of luxury, privacy, convenience, and comfort amidst the din and bustle of the city. 
            </p><p>
            </p><p>Prince International Plaza is ideally located along Russian Federation Boulevard and in the intersection of three major districts: Toul Kork, Meanchey, and Sen Sok. These are some of the best locations that could be found anywhere in Phnom Penh. 
            </p><p>
            </p><p>Its location means that important amenities are at its residents&amp;rsquo; doorstep. Canadia Tower, Vattanac Tower, Parkson Shopping Mall, TK Avenue, Cambodia Railways, Wat Phnom, RUPP, embassies, international schools, hospitals, government agencies, hotels, and other centers of commerce, transportations, tourism, etc. are close by. The Phnom Penh International Airport, AEON Mall 1 and 2, and railway station can be reached by car in less than 30 minutes.</p></span></div>
